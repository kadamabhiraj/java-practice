public class practice_4 {
    public static void main (String args[]){
        byte a = 10;
        System.out.println(a);
        char b = 'C';
        System.out.println(b);
        boolean c = true;
        System.out.println(c);
        float d = 4.4F;
        System.out.println(d);
        int e = 10;
        System.out.println(e);
        long f = 100000000L;
        System.out.println(f);
        double g = 3.142D;
        System.out.println(g);
    }
}
